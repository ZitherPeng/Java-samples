package com.lc.jsc.datatypes;

public class IntegerTypes {

    /**
     * int: 4bytes -2,147,483,648（-2^31）to 2,147,483,647（2^31 - 1） default 0
     */
    int fooInt = 10000000; int barInt = -20000000;
    
    /**
     * short: 2bytes -32768（-2^15）to 32767（2^15 - 1) default 0
     */
    short fooShort = 10000; int barShort = -10000;

    /**
     * long: 8bytes -9,223,372,036,854,775,808（-2^63）to 9,223,372,036,854,775,807（2^63 -1） default 0L
     */
    long fooLong = 1000000000L; long barLong = -200000000L;

}
